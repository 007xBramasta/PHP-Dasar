<?php

//AND
var_dump(true && true); //jika dua dua nya true akan TRUE jika tidak makan FALSE
var_dump(true && false);


//NOR
var_dump(true || true); //salah satunya ada yang true makan TRUE
var_dump(true || false);


//XOR
var_dump(true xor false); //salah satunya true maka TRUE jika dua dua nya true maka FALSE
var_dump(true xor true);

//NOT
var_dump(!true); //jika true maka FALSE
var_dump(!false); //jika false maka TRUE